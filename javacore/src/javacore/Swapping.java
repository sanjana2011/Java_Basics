package javacore;

public class Swapping {

		public static void main(String[] args) {

			int a = 5;
			int b = 10;
			int c;

			System.out.println("before swaping ==== ");
			System.out.println("a = " + a);
			System.out.println("b = " + b);

			c = a; // c = 5;
			a = b; // a = 10;
			b = c; // b = 5;

			System.out.println("after swaping ==== ");
			System.out.println("a = " + a);
			System.out.println("b = " + b);

		}

	}

